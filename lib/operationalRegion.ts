function isInsideDelhiBoundary(lat: number, lng: number) {
  let inside = false;

  for (let i = 0, j = DELHI_NCT_BOUNDARY.length - 1; i < DELHI_NCT_BOUNDARY.length; j = i++) {
    const [lngI, latI] = DELHI_NCT_BOUNDARY[i];
    const [lngJ, latJ] = DELHI_NCT_BOUNDARY[j];
    const crossesLatitude = latI > lat !== latJ > lat;
    const boundaryLng = ((lngJ - lngI) * (lat - latI)) / (latJ - latI) + lngI;

    if (crossesLatitude && lng < boundaryLng) inside = !inside;
  }

  return inside;
}

export function isInOperationalRegion(lat: number, lng: number) {
  if (!Number.isFinite(lat) || !Number.isFinite(lng)) return false;
  if (
    lat < DELHI_OPERATIONAL_BOUNDS.minLat ||
    lat > DELHI_OPERATIONAL_BOUNDS.maxLat ||
    lng < DELHI_OPERATIONAL_BOUNDS.minLng ||
    lng > DELHI_OPERATIONAL_BOUNDS.maxLng
  ) {
    return false;
  }

  return isInsideDelhiBoundary(lat, lng);
}
